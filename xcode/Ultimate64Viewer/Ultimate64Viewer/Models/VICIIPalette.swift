import Foundation

struct VICIIPalette {
    private static let rgbValues: [(UInt8, UInt8, UInt8)] = [
        (0x00, 0x00, 0x00), // Black
        (0xEF, 0xEF, 0xEF), // White
        (0x8D, 0x2F, 0x34), // Red
        (0x6A, 0xD4, 0xCD), // Cyan
        (0x98, 0x35, 0xA4), // Purple
        (0x4C, 0xB4, 0x42), // Green
        (0x2C, 0x29, 0xB1), // Blue
        (0xEF, 0xEF, 0x5D), // Yellow
        (0x98, 0x4E, 0x20), // Orange
        (0x5B, 0x38, 0x00), // Brown
        (0xD1, 0x67, 0x6D), // Light Red
        (0x4A, 0x4A, 0x4A), // Dark Grey
        (0x7B, 0x7B, 0x7B), // Grey
        (0x9F, 0xEF, 0x93), // Light Green
        (0x6D, 0x6A, 0xEF), // Light Blue
        (0xB2, 0xB2, 0xB2), // Light Grey
    ]
    
    static func getRGBComponents(for colorIndex: UInt8) -> (r: UInt8, g: UInt8, b: UInt8) {
        let index = Int(colorIndex) % rgbValues.count
        return rgbValues[index]
    }
}